import { IsString, IsOptional, IsNumber, IsDate } from 'class-validator';

export class UpdateBookDto {
  @IsOptional()
  @IsString()
  title?: string;

  @IsOptional()
  @IsString()
  author?: string;

  @IsOptional()
  @IsString()
  category?: string;

  @IsOptional()
  @IsNumber()
  price?: number;

  @IsOptional()
  @IsNumber()
  rating?: number;

  @IsOptional()
  @IsDate()
  published_date?: Date;
}
